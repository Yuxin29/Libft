int	ft_atoi(const char *nptr)
{
	int	i;
	int	out;

	i = 0;
	out = 0;
	if ((nptr[i] < '0') && (nptr[i] > '9'))
		return (0);
	while (('0' <= nptr[i]) && (nptr[i] <= '9'))
	{
		out = out * 10 + (nptr[i] - '0');
		i++;
	}
	return (out);
}
